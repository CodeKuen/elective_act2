let x = 9;
let y = 11;

function subtract(x, y) {
    return x - y;
}
console.log("The difference of " + x + " and " + y + " is: " + subtract(x, y));

function multiply(x, y) {
    return x * y;
}
console.log("The product of " + x + " and " + y + " is: " + multiply(x, y));

function divide(x, y) {
    return x / y;
}
console.log("The quotient of " + x + " and " + y + " is: " + divide(x, y));

function squareNumber(num) {
    return num * num;
}
console.log("The square of " + x + " is: " + squareNumber(x));
console.log("The square of " + y + " is: " + squareNumber(y));